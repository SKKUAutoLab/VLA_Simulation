def get_closest_waypoint(x: float, y: float) -> tuple:
    """현재 위치에서 가장 가까운 트랙 중앙선 웨이포인트 반환."""
    min_dist = float('inf')
    closest = TRACK_CENTERLINE[0]
    for wp in TRACK_CENTERLINE:
        dist = math.hypot(wp[0] - x, wp[1] - y)
        if dist < min_dist:
            min_dist = dist
            closest = wp
    return closest


def get_waypoints_ahead(x: float, y: float, n: int = 10) -> list:
    """현재 위치에서 앞쪽 n개 웨이포인트 반환."""
    start = 0
    min_dist = float('inf')
    for i, wp in enumerate(TRACK_CENTERLINE):
        dist = math.hypot(wp[0] - x, wp[1] - y)
        if dist < min_dist:
            min_dist = dist
            start = i
    result = []
    for i in range(n):
        result.append(TRACK_CENTERLINE[(start + i) % len(TRACK_CENTERLINE)])
    return result
